  /**
   * Carga costos auto-generados por el IntegrationHub desde localStorage
   * y los fusiona con los costos en memoria (evitando duplicados)
   */
  private mergeAutoGeneratedCosts(): void {
    if (typeof window === 'undefined') return;
    try {
      const raw = localStorage.getItem('tms-auto-costs');
      if (!raw) return;
      const autoCosts: TransportCost[] = JSON.parse(raw);
      const existingIds = new Set(this.costs.map(c => c.id));
      const newCosts = autoCosts.filter(c => !existingIds.has(c.id));
      if (newCosts.length > 0) {
        this.costs.push(...newCosts);
      }
    } catch {
      // Ignorar errores de parse
    }
  }

  /**
   * Carga facturas auto-generadas por el IntegrationHub desde localStorage
   * y las fusiona con las facturas en memoria (evitando duplicados)
   */
  private mergeAutoGeneratedInvoices(): void {
    if (typeof window === 'undefined') return;
    try {
      const raw = localStorage.getItem('tms-auto-invoices');
      if (!raw) return;
      const autoInvoices: Invoice[] = JSON.parse(raw);
      const existingIds = new Set(this.invoices.map(i => i.id));
      const newInvoices = autoInvoices.filter(i => !existingIds.has(i.id));
      if (newInvoices.length > 0) {
        this.invoices.push(...newInvoices);
      }
    } catch {
      // Ignorar errores de parse
    }
  }

  private async simulateDelay(ms: number = 200): Promise<void> {
    if (this.useMocks) {
      await new Promise(resolve => setTimeout(resolve, ms));
    }
  }

  private generateId(prefix: string): string {
    return `${prefix}-${Date.now()}-${Math.random().toString(36).substring(2, 9)}`;
  }

  private generateNumber(prefix: string): string {
    const year = new Date().getFullYear();
    const seq = String(Math.floor(Math.random() * 99999)).padStart(5, "0");
    return `${prefix}-${year}-${seq}`;
  }

