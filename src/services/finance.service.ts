  async createInvoice(data: CreateInvoiceDTO): Promise<Invoice> {
    await this.simulateDelay(300);

    if (this.useMocks) {
      const now = new Date().toISOString();
      
      // Calcular totales
      let subtotal = 0;
      let taxTotal = 0;
      let discountTotal = 0;

      const lineItems: InvoiceLineItem[] = data.lineItems.map((item, idx) => {
        const itemSubtotal = item.quantity * item.unitPrice;
        const discount = item.discountType === "percentage"
          ? itemSubtotal * (item.discount / 100)
          : item.discount;
        const taxableAmount = itemSubtotal - discount;
        const taxAmount = taxableAmount * (item.taxRate / 100);
        const total = taxableAmount + taxAmount;

        subtotal += itemSubtotal;
        taxTotal += taxAmount;
        discountTotal += discount;

        return {
          ...item,
          id: `li-${Date.now()}-${idx}`,
          taxAmount,
          subtotal: itemSubtotal,
          total,
        };
      });

      const invoice: Invoice = {
        id: this.generateId("inv"),
        invoiceNumber: this.generateNumber("INV"),
        type: data.type,
        status: "draft",
        customerId: data.customerId,
        customerName: `Cliente ${data.customerId}`,
        issueDate: data.issueDate || now,
        dueDate: data.dueDate,
        currency: data.currency || "PEN",
        subtotal,
        taxTotal,
        discountTotal,
        total: subtotal - discountTotal + taxTotal,
        amountPaid: 0,
        amountDue: subtotal - discountTotal + taxTotal,
        lineItems,
        taxes: [{ 
          id: "tax-auto", 
          name: "IGV", 
          code: "IGV", 
          rate: 18, 
          amount: taxTotal, 
          isInclusive: false 
        }],
        orderIds: data.orderIds,
        purchaseOrderNumber: data.purchaseOrderNumber,
        notes: data.notes,
        termsAndConditions: data.termsAndConditions,
        createdBy: "system",
        createdAt: now,
        updatedAt: now,
      };

      this.invoices.unshift(invoice);
      return invoice;
    }

    return apiClient.post<Invoice>(API_ENDPOINTS.finance.invoices, data);
  }

